import math


def area(r):
    """
    Returns area of circle

    :param r: radius of a circle
    :return: area of a circle
    """
    return math.pi * r * r


def perimeter(r):
    """
    Returns perimeter of circle

    :param r: radius of circle
    :return: perimeter of circle
    """
    return 2 * math.pi * r

